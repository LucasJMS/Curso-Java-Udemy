package api;

public class ExplorandoMath {
	
	public static void main(String[] args) {
		
		System.out.println(Math.PI);
		System.out.println(Math.pow(3, 2));
		System.out.println(Math.abs(-10.5));
		System.out.println(Math.min(-14, -17));
		System.out.println(Math.max(-14, -17));
		System.out.println(Math.ceil(5.55)); // Prof. Legal
		System.out.println(Math.ceil(5.55)); // Prof. Chato
		System.out.println(Math.random());
		// Math.
		
	}


	
}
